package model.clothes;

/**
 *
 * @author phamt
 */
public class Clothes {

    private int id;
    private int typeId;
    private String name;
    private String branch;
    private String img;
    private String size;
    private String color;
    private String summary;

    public Clothes() {
    }

    public Clothes(int id, int typeId, String name, String img, String branch, String size, String color, String summary) {
        super();
        this.id = id;
        this.typeId = typeId;
        this.name = name;
        this.img = img;
        this.branch = branch;
        this.size = size;
        this.color = color;
        this.summary = summary;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }
    
    
}
